print("Programa Que Calcule La Distancia Entre Dos Puntos")
X1 = float(input("Ingresa las Coordenadas del Punto 1 X1: "))
Y1 = float(input("Ingresa las Coordenadas del Punto 1 Y1: "))
X2 = float(input("Ingresa las Coordenadas del Punto 2 X2: "))
Y2 = float(input("Ingresa las Coordenadas del Punto 2 Y2: "))
DIS = ((X1-X2) ** 2 + (Y1-Y2) ** 2) ** 0.5
print(f"La Distancia Entre Los Dos Puntos Dados Es: {DIS} ")

